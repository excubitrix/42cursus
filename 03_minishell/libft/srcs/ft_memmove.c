#include "libft.h"

void		*ft_memmove(void *dest, const void *src, size_t n)
{
	if (dest < src)
		ft_memcpy(dest, src, n);
	else if (dest > src)
		ft_memrcpy(dest, src, n);
	return (dest);
}
