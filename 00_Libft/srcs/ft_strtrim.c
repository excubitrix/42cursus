#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	len = ft_strlen(s1 + start);
	if (len)
		while (s1[start + len - 1]
			&& ft_strchr(set, s1[start + len - 1]))
			len--;
	return (ft_substr(s1, start, len));
}
