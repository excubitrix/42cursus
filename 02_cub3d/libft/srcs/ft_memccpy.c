#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	void *ptr;

	ptr = ft_memchr(src, c, n);
	if (ptr)
		return (ft_memcpy(dest, src, ptr - src + 1) + (ptr - src + 1));
	ft_memcpy(dest, src, n);
	return (NULL);
}
