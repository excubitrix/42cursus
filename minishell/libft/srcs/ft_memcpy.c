#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*csrc;
	unsigned char	*cdest;

	csrc = (unsigned char *)src;
	cdest = (unsigned char *)dest;
	if (dest == NULL && src == NULL)
		return (NULL);
	while (n--)
		*cdest++ = *csrc++;
	return (dest);
}
